--FOLOSIND UN CURSOR SA SE AFISEZE NUMELE SI PRENUMELE CLIENT 
--CARE AU DAT CEL PIUTIN 2 COMENZI
--CLIENTII SE VOR AFISA IN ORDINEA DESC A LIMIT CREDIT

--NUME_CURORS%ISOPEN
--NUME_CURSOR%FOUND
--NUME_CURSOR%NOTFOUND
--NUME_CURSOR%ROWCOUNT

SET SERVEROUTPUT ON

SELECT NUME_CLIENT,PRENUME_CLIENT
FROM CLIENTI
WHERE ID_CLIENT IN ( SELECT ID_CLIENT 
FROM COMENZI 
GROUP BY ID_CLIENT
HAVING COUNT(*)>=2);

--PARCURGERE CLASICA CU OPEN-LOOP-FETCH-CLOSE

DECLARE 
    CURSOR C IS SELECT NUME_CLIENT,PRENUME_CLIENT,LIMITA_CREDIT
                    FROM CLIENTI
                    WHERE ID_CLIENT IN ( SELECT ID_CLIENT 
                    FROM COMENZI 
                    GROUP BY ID_CLIENT
                    HAVING COUNT(*)>=2)
                    ORDER BY LIMITA_CREDIT DESC;
    V_NUME_CLIENT CLIENTI.NUME_CLIENT%TYPE;
    V_PRENUME_CLIENT CLIENTI.PRENUME_CLIENT%TYPE;
    V_LIMITA_CREDIT CLIENTI.LIMITA_CREDIT%TYPE;
BEGIN
    IF NOT C%ISOPEN THEN
        OPEN C;
    END IF;
    
    LOOP
    
    FETCH C INTO  V_NUME_CLIENT,V_PRENUME_CLIENT,V_LIMITA_CREDIT; --FETCH IA PRIMUL RAND DIN SELECT SI IL PUNE IN VARIABILE
    EXIT WHEN C%NOTFOUND; --iesim din loop cand fetch nu mai gaseste nimic
    DBMS_OUTPUT.PUT_LINE(C%ROWCOUNT||' '||V_NUME_CLIENT||' '||V_PRENUME_CLIENT||' '||V_LIMITA_CREDIT);
    END LOOP;
    
    CLOSE C;

END;
/

