--FOLOSIND UN CURSOR SA SE AFISEZE DENUMIREA DEPARTAMENTELOR UNDE TOTI ANGAJATII AU SALARILE <=10000
DECLARE 
    CURSOR C IS SELECT DENUMIRE_DEPARTAMENT
                FROM DEPARTAMENTE
                WHERE ID_DEPARTAMENT IN(
                SELECT ID_DEPARTAMENT 
                FROM ANGAJATI
                GROUP BY ID_DEPARTAMENT
                HAVING MAX(SALARIUL)<=10000);
    R C%ROWTYPE;
BEGIN 
IF NOT C%ISOPEN THEN
        OPEN C;
    END IF;
    
    LOOP
    
    FETCH C INTO R;
    EXIT WHEN C%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE(C%ROWCOUNT||' '||R.DENUMIRE_DEPARTAMENT);
    END LOOP;
    
    DBMS_OUTPUT.PUT_LINE('Nr departamente:'||C%ROWCOUNT);
    
    CLOSE C;
END;
/

