--FOLOSIND UN CURSOR, SA SE AFISEZE DENUMIREA CATEGORIILOR DE PRODUSE SI VALOAREA TOTALA A COMENZILOR
--DIN PERSPECTIVELE CATEGORII. SE VOR AFISA DOAR CATEG CU VAL TOTALA A COMENZILOR >500000


DECLARE
    CURSOR C IS SELECT 
    P.CATEGORIE,
    SUM(R.PRET *R.CANTITATE) AS VAL_T
    FROM PRODUSE P
    JOIN RAND_COMENZI R ON P.ID_PRODUS=R.ID_PRODUS
    GROUP BY P.CATEGORIE
    HAVING SUM(R.PRET *R.CANTITATE)>50000;
BEGIN
    FOR R IN C LOOP 
    DBMS_OUTPUT.PUT_LINE(R.CATEGORIE);
    END LOOP;
END;
/

