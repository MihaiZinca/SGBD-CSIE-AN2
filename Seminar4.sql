    V T_ANG;
    I PLS_INTEGER;
    
BEGIN
    SELECT *
    BULK COLLECT INTO V -- 
    FROM ANGAJATI
    WHERE ID_FUNCTIE = 'SA_REP'
    AND SALARIUL BETWEEN 5000 AND 9000
    ORDER BY SALARIUL DESC;
   
