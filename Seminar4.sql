    DBMS_OUTPUT.PUT_LINE('NR='||V.COUNT);
    
END;
/


DECLARE
    TYPE T_ANG IS TABLE OF VARCHAR2(100) INDEX BY PLS_INTEGER; 
    V T_ANG; 
BEGIN
    V(1):='ION';
    V(2):='MARIUS';
    V(3):='MARIA';
    V(40):='ANA';
    
    FOR I in V.FIRST .. V.LAST LOOP
        CONTINUE WHEN NOT V.EXISTS(I); --daca nu exista trecre la urmatorul
        DBMS_OUTPUT.PUT_LINE(I||'->'||V(I));
    END LOOP;
    
END;
